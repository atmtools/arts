//! CKD version MT 3.50 H2O foreign continuum absorption model

/*!

Changes from the release notes:

The following modifications to the self water vapor continuum arise from:

October 2020: The water vapor continuum in the microwave through far-infrared was modified 
(E. Mlawer, V. Payne, J. Mascio, D. Turner). The modifications will be detailed in Payne et al. (2021).

   \param[inout] propmat_clearsky The propagation matrix  [1/m]
   \param[in]    f_grid           frequency grid          [Hz]
   \param[in]    P                pressure                [Pa]
   \param[in]    T                temperature             [K]
   \param[in]    vmrh2o           H2O volume mixing ratio [1]

   \note     This absorption model is taken from the FORTRAN77 code of
             CKD_MT version 3.50 contnm.f90 written by<br>
             Atmospheric and Environmental Research Inc. (AER),<br>
             Radiation and Climate Group<br>
             131 Hartwell Avenue<br>
             Lexington, MA 02421, USA<br>
             http://www.rtweb.aer.com/continuum_frame.html

   \author Stuart Fox
   \date 2021-09-16
*/
void compute_foreign_h2o(PropmatVector& propmat_clearsky,
                         const Vector& f_grid,
                         const Numeric& P,
                         const Numeric& T,
                         const Numeric& vmrh2o) {
  using Conversion::freq2kaycm;
  const Index nf = f_grid.nelem();

  //     Foreign correction factors from joint RHUBC-II/RHUBC-I
  //     analysis (mt_ckd_3.0) for up to 600cm-1
  //     Modified from new MR analysis (Payne et al., 2021, mt_ckd 3.5)
  constexpr std::array XFAC_RHU = {
      0.7620, 0.7840, 0.7820, 0.7840, 0.7620, 0.7410, 0.7970, 0.9140, 0.9980,
      0.9830, 0.9330, 0.8850, 0.8420, 0.8070, 0.8000, 0.8010, 0.8100, 0.8090,
      0.8320, 0.8180, 0.7970, 0.8240, 0.8640, 0.8830, 0.8830, 0.8470, 0.8380,
      0.8660, 0.9410, 1.0400, 1.0680, 1.1410, 1.0800, 1.0340, 1.1550, 1.0990,
      1.0270, 0.9500, 0.8950, 0.8150, 0.7830, 0.7700, 0.7000, 0.7650, 0.7750,
      0.8500, 0.9000, 0.9050, 0.9540, 1.0200, 1.0200, 1.0250, 1.0200, 1.1000,
      1.1250, 1.1200, 1.1110, 1.1370, 1.1600, 1.1490, 1.1070, 1.0640, 1.0450};

  // It is assumed here that f_grid is monotonically increasing with index!
  // In future change this return into a change of the loop over
  // the frequency f_grid. n_f_new < n_f
  const Numeric V1ABS = freq2kaycm(f_grid[0]);       // [cm^-1]
  const Numeric V2ABS = freq2kaycm(f_grid[nf - 1]);  // [cm^-1]

  // ---------------------- subroutine FRN296 ------------------------------

  // retrieve the appropriate array sequence of the foreign continuum
  // arrays of the CKD model.
  const Numeric DVC = FH2O_ckd_mt_350_dv;
  Numeric V1C = V1ABS - DVC;
  Numeric V2C = V2ABS + DVC;

  int I1 = (int)((V1C - FH2O_ckd_mt_350_v1) / FH2O_ckd_mt_350_dv);
  if (V1C < FH2O_ckd_mt_350_v1) I1 = -1;
  V1C = FH2O_ckd_mt_350_v1 + (FH2O_ckd_mt_350_dv * (Numeric)(I1 - 1));

  const int I2 = (int)((V2C - FH2O_ckd_mt_350_v1) / FH2O_ckd_mt_350_dv);

  int NPTC = I2 - I1 + 3;
  if (NPTC > FH2O_ckd_mt_350_npt) NPTC = FH2O_ckd_mt_350_npt + 4;

  V2C = V1C + FH2O_ckd_mt_350_dv * (Numeric)(NPTC - 1);

  if (NPTC < 1) {
    return;
  }

  Vector FH2OT0(NPTC + addF77fields, 0.);  // [cm^3/molecules]

  for (Index J = 1; J <= NPTC; ++J) {
    const Index I = I1 + (J - 1);
    if ((I > 0) && (I <= FH2O_ckd_mt_350_npt)) {
      FH2OT0[J] = FH2O_ckd_mt_350[I];
    }
  }

  // ---------------------- subroutine FRN296 ------------------------------

  // atmospheric state parameters
  const Numeric Pave = (P * 1.000e-2);                       // [hPa]
  const Numeric PFRGN = (Pave / PO) * (1.00000e0 - vmrh2o);  // dry air pressure [hPa]
  const Numeric RFRGN = PFRGN * (TO / T);                    // [hPa]
  const Numeric WTOT = xLosmt * (Pave / PO) * (T1 / T);      // [molecules/cm^2]
  const Numeric W1 = vmrh2o * WTOT;                          // [molecules/cm^2]
  const Numeric XKT = T / 1.4387752;                         // = (T*k_B) / (h*c)

  // Molecular cross section calculated by CKD.
  // The cross sectionis calculated on the predefined
  // CKD wavenumber grid.
  Vector k(NPTC + addF77fields, 0.);  // [1/cm]
  for (Index J = 1; J <= NPTC; ++J) {
    const Numeric VJ = V1C + (DVC * (Numeric)(J - 1));

    int JFAC = 0;
    Numeric FSCAL = 0;

    if (VJ < 600e0)  // New condition for MT-CKD 3.2
    {
      // Scaling factor below 600cm-1 based on RHUBC-II campaign
      JFAC = (int)((VJ + 10e0) / 10e0 + 0.00001e0);
      FSCAL = XFAC_RHU[JFAC + 1];  // +1 needed as C-array is zero indexed
                                   // but FORTRAN code has DIMENSION -1, 61
    } else  // same as from version 2.4 apart from the 630e0 in the VF2 equation
    {
      const Numeric VDELSQ1 = pow((VJ - 255.67e0), 2e0);
      const Numeric VDELmSQ1 = pow((VJ + 255.67e0), 2e0);
      const Numeric VF1 = pow(((VJ - 255.67e0) / 57.83e0), 8e0);
      const Numeric VmF1 = pow(((VJ + 255.67e0) / 57.83e0), 8e0);
      const Numeric VF2 = pow(
          ((VJ) / 630e0),
          8e0);  // the 630e0 factor is different from previous versions of VF2 equation

      FSCAL = 1.000e0 +
              (0.06e0 + (-0.42e0) * ((57600e0 / (VDELSQ1 + 57600e0 + VF1)) +
                                     (57600e0 / (VDELmSQ1 + 57600e0 + VmF1)))) /
                  (1.000e0 + 0.3e0 * VF2);
    }

    const Numeric FH2O = FH2OT0[J] * FSCAL;

    // CKD cross section with radiative field [1/cm]
    k[J] = W1 * RFRGN * (FH2O * 1.000e-20) * RADFN_FUN(VJ, XKT);
  }

  // Loop input frequency array. The previously calculated cross section
  // has therefore to be interpolated on the input frequencies.
  for (Index s = 0; s < nf; ++s) {
    // calculate the associated wave number (= 1/wavelength)
    const Numeric V = freq2kaycm(f_grid[s]);  // [cm^-1]
    if ((V >= 0.000e0) && (V < FH2O_ckd_mt_350_v2)) {
      // arts CKD_MT.100 cross section [1/m]
      // interpolate the k vector on the f_grid grid
      // The factor 100 comes from the conversion from (1/cm) to (1/m)
      // of the abs. coeff.
      propmat_clearsky[s].A() += 1.000e2 * XINT_FUN(V1C, V2C, DVC, k, V);
    }
  }
}
