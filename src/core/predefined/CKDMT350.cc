Numeric RADFN_FUN(const Numeric XVI, const Numeric XKT) {
  // ---------------------------------------------------------------------- B18060
  //              LAST MODIFICATION:    12 AUGUST 1991                      B17940
  //                                                                        B17950
  //                 IMPLEMENTATION:    R.D. WORSHAM                        B17960
  //                                                                        B17970
  //            ALGORITHM REVISIONS:    S.A. CLOUGH                         B17980
  //                                    R.D. WORSHAM                        B17990
  //                                    J.L. MONCET                         B18000
  //                                                                        B18010
  //                                                                        B18020
  //                    ATMOSPHERIC AND ENVIRONMENTAL RESEARCH INC.         B18030
  //                    840 MEMORIAL DRIVE,  CAMBRIDGE, MA   02139          B18040
  //                                                                        B18050
  //                                                                        B18070
  //              WORK SUPPORTED BY:    THE ARM PROGRAM                     B18080
  //                                    OFFICE OF ENERGY RESEARCH           B18090
  //                                    DEPARTMENT OF ENERGY                B18100
  //                                                                        B18110
  //                                                                        B18120
  //     SOURCE OF ORIGINAL ROUTINE:    AFGL LINE-BY-LINE MODEL             B18130
  //                                                                        B18140
  //                                            FASCOD3                     B18150
  //                                                                        B18160
  // ---------------------------------------------------------------------- B18060
  //                                                                        B18170
  //  IN THE SMALL XVIOKT REGION 0.5 IS REQUIRED

  Numeric RADFN = 0.00e0;

  if (XKT > 0.0) {
    const Numeric XVIOKT = XVI / XKT;

    if (XVIOKT <= 0.01e0) {
      RADFN = 0.500e0 * XVIOKT * XVI;
    } else if (XVIOKT <= 10.0e0) {
      Numeric EXPVKT = std::exp(-XVIOKT);
      RADFN = XVI * (1.00e0 - EXPVKT) / (1.00e0 + EXPVKT);
    } else {
      RADFN = XVI;
    }
  } else {
    RADFN = XVI;
  }

  return RADFN;
}

Numeric XINT_FUN(const Numeric V1A,
                 const Numeric /* V2A */,
                 const Numeric DVA,
                 const Vector& A,
                 const Numeric VI) {
  // ----------------------------------------------------------------------
  //     THIS SUBROUTINE INTERPOLATES THE A ARRAY STORED
  //     FROM V1A TO V2A IN INCREMENTS OF DVA INTO XINT
  // ----------------------------------------------------------------------

  constexpr Numeric ONEPL = 1.001;  // original value given in F77 code

  const Numeric RECDVA = 1.00e0 / DVA;

  const int J = (int)((VI - V1A) * RECDVA + ONEPL);
  const Numeric VJ = V1A + DVA * (Numeric)(J - 1);
  const Numeric P = RECDVA * (VI - VJ);
  const Numeric C = (3.00e0 - 2.00e0 * P) * P * P;
  const Numeric B = 0.500e0 * P * (1.00e0 - P);
  const Numeric B1 = B * (1.00e0 - P);
  const Numeric B2 = B * P;

  Numeric xint = 0.;
  if (J - 1 > 0 && J + 2 < A.nelem()) {
    xint = -A[J - 1] * B1 + A[J] * (1.00e0 - C + B2) + A[J + 1] * (C + B1) -
           A[J + 2] * B2;
  }

  return xint;
}

//! CKD version MT 3.50 H2O self continuum absorption model
/*!
     The following modifications to the self water vapor continuum arise from:

     October 2020: The water vapor continuum in the microwave through far-infrared was modified 
     (E. Mlawer, V. Payne, J. Mascio, D. Turner). The modifications will be detailed in Payne et al. (2021).
     
     August 2017: New coefficients for self continuum in window regions for wavenumbers greater than 2000 cm-1 
     were added and self continuum temperature dependence from 1800-3500 cm-1 were updated (E.J. Mlawer, 
     M.J. Alvarado, K.E. Cady-Pereira). 

     November 2016: SFAC re-adjusted based on RHUBC-II campaign. (Mlawer, E.J. et al. 2018
     Analysis  of  water  vapor  absorption  in  the  far-infrared  and  submillimeter  regions 
     using surface radiometric measurements fromextremely dry locations, submitted)

     July 2016: Self continuum coefficients were modified from 1880-2390 cm-1 to fix issues in the CO fundamental 
     region that were pointed out by Alvarado et al. (2013).

   \param[inout] propmat_clearsky The propagation matrix  [1/m]
   \param[in]    f_grid           frequency grid          [Hz]
   \param[in]    P                pressure                [Pa]
   \param[in]    T                temperature             [K]
   \param[in]    vmrh2o           H2O volume mixing ratio [1]

   \note     This absorption model is taken from the FORTRAN code of
             CKD_MT version 3.50 contnm.f90 written by<br>
             Atmospheric and Environmental Research Inc. (AER),<br>
             Radiation and Climate Group<br>
             131 Hartwell Avenue<br>
             Lexington, MA 02421, USA<br>
             http://www.rtweb.aer.com/continuum_frame.html

   \author   Stuart Fox
   \date     2021-09-15
*/
void compute_self_h2o(PropmatVector& propmat_clearsky,
                      const Vector& f_grid,
                      const Numeric& P,
                      const Numeric& T,
                      const Numeric& vmrh2o) {
  using Conversion::freq2kaycm;
  const Index nf = f_grid.nelem();

  // It is assumed here that f_grid is monotonically increasing with index!
  // In future change this return into a change of the loop over
  // the frequency f_grid. n_f_new < n_f
  const Numeric V1ABS = freq2kaycm(f_grid[0]);       // [cm^-1]
  const Numeric V2ABS = freq2kaycm(f_grid[nf - 1]);  // [cm^-1]

  // retrieve the appropriate array sequence of the self continuum
  // arrays of the CKD model.
  const Numeric DVC = SL296_ckd_mt_350_dv;
  Numeric V1C = V1ABS - DVC;
  Numeric V2C = V2ABS + DVC;

  int I1 = (int)((V1C - SL296_ckd_mt_350_v1) / SL296_ckd_mt_350_dv);
  if (V1C < SL296_ckd_mt_350_v1) I1 = -1;
  V1C = SL296_ckd_mt_350_v1 + (SL296_ckd_mt_350_dv * (Numeric)(I1 - 1));

  const int I2 = (int)((V2C - SL296_ckd_mt_350_v1) / SL296_ckd_mt_350_dv);

  int NPTC = I2 - I1 + 3;
  if (NPTC > SL296_ckd_mt_350_npt) NPTC = SL296_ckd_mt_350_npt + 4;

  V2C = V1C + SL296_ckd_mt_350_dv * (Numeric)(NPTC - 1);

  if (NPTC < 1) {
    return;
  }

  Vector SH2OT0(NPTC + addF77fields, 0.);  // [cm^3/molecules]
  Vector SH2OT1(NPTC + addF77fields, 0.);  // [cm^3/molecules]

  for (Index J = 1; J <= NPTC; ++J) {
    const Index I = I1 + (J - 1);
    if ((I > 0) && (I <= SL296_ckd_mt_350_npt)) {
      SH2OT0[J] = SL296_ckd_mt_350[I];  // at T=296 K
      SH2OT1[J] = SL260_ckd_mt_350[I];  // at T=260 K
    }
  }

  // ------------------- subroutine SL296/SL260 ----------------------------

  const Numeric Pave = (P * 1.000e-2);                                 // [hPa]
  const Numeric Patm = Pave / PO;                                      // [1]
  const Numeric Rh2o = vmrh2o * Patm * (TO / T);                       // [1]
  const Numeric Tfac = (T - TO) / (260.0 - TO);                        // [1]
  const Numeric WTOT = xLosmt * (Pave / 1.013000e3) * (2.7300e2 / T);  // [molecules/cm^2]
  const Numeric W1 = vmrh2o * WTOT;                                    // [molecules/cm^2]
  const Numeric XKT = T / 1.4387752e0;                                 // = (T*k_B)/(h*c)

  // Molecular cross section calculated by CKD.
  // The cross sectionis calculated on the predefined
  // CKD wavenumber grid.
  Vector k(NPTC + addF77fields, 0.);  // [1/cm]
  for (Index J = 1; J <= NPTC; ++J) {
    const Numeric VJ = V1C + (DVC * (Numeric)(J - 1));
    Numeric SH2O = 0.0e0;
    if (SH2OT0[J] > 0.0e0) {
      SH2O = SH2OT0[J] * pow((SH2OT1[J] / SH2OT0[J]), Tfac);
    }

    // CKD cross section with radiative field [1/cm]
    k[J] = W1 * Rh2o * (SH2O * 1.000e-20) * RADFN_FUN(VJ, XKT);
  }

  // Loop input frequency array. The previously calculated cross section
  // has therefore to be interpolated on the input frequencies.
  for (Index s = 0; s < nf; ++s) {
    // calculate the associated wave number (= 1/wavelength)
    const Numeric V = freq2kaycm(f_grid[s]);  // [cm^-1]
    if ((V > 0.000e0) && (V < SL296_ckd_mt_350_v2)) {
      // arts cross section [1/m]
      // interpolate the k vector on the f_grid grid
      // The factor 100 comes from the conversion from 1/cm to 1/m for
      // the absorption coefficient
      propmat_clearsky[s].A() += 1.000e2 * XINT_FUN(V1C, V2C, DVC, k, V);
    }
  }
}

